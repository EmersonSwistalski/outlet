     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cupom = Cupom::where('nome', $request->cupom)->get();
        
        $produtoCupom = new ProdutoCupom;
        $produtoCupom->produto_id = $request->produto_id;
        $produtoCupom->cupom_id = $cupom[0]->id;

        if ($produtoCupom->save()){
            return "Success";
        }

    }

